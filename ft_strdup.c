#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*string;
	int		i;

	if (!s)
		return (NULL);
	string = malloc(sizeof(char) * (ft_strlen(s) + 1));
	if (!string)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		string[i] = s[i];
		i++;
	}
	string[i] = '\0';
	return (string);
}
